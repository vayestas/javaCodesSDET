package class7;

public class E9Arrays {
    public static void main(String[] args) {
        String [] fruits={"Apple","Orange","Banana","Kiwi","Pineapple"};
        System.out.println(fruits.length); //gives us number of items in array
        for (int i = 0; i < fruits.length; i++) {
            System.out.println(fruits[i]);
        }
    }
}
